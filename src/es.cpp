void prepara_entrada(){
#ifdef WINDOWS
	DWORD estat;
	HandleEntrada = GetStdHandle(STD_INPUT_HANDLE);
	Consola = !GetConsoleMode(HandleEntrada, &estat);
	if (Consola) {
		SetConsoleMode(HandleEntrada, estat & (~(ENABLE_MOUSE_INPUT | ENABLE_WINDOW_INPUT)));
		FlushConsoleInputBuffer(HandleEntrada);
	}

	setbuf(stdout, NULL);
#endif
}

int entrada_pendent() {
#ifdef WINDOWS
	DWORD dw;
	if (stdin->_cnt > 0) return 1;
	if (Consola) {
		if (!PeekNamedPipe(HandleEntrada, NULL, 0, NULL, &dw, NULL))
			return 1;
		else return (dw != 0);
	}
	else return _kbhit() != 0;
#endif
#ifdef LINUX
	/*//http://stackoverflow.com/questions/717572/how-do-you-do-non-blocking-console-i-o-on-linux-in-c
	struct timeval tv;
	fd_set fds;
	tv.tv_sec = 0;
	tv.tv_usec = 0;
	FD_ZERO(&fds);
	FD_SET(STDIN_FILENO, &fds);
	select(STDIN_FILENO + 1, &fds, NULL, NULL, &tv);
	return (FD_ISSET(0, &fds));*/

	struct termios oldt, newt;
	int ch;
	int oldf;

	tcgetattr(STDIN_FILENO, &oldt);
	newt = oldt;
	newt.c_lflag &= ~(ICANON | ECHO);
	tcsetattr(STDIN_FILENO, TCSANOW, &newt);
	oldf = fcntl(STDIN_FILENO, F_GETFL, 0);
	fcntl(STDIN_FILENO, F_SETFL, oldf | O_NONBLOCK);

	ch = getchar();

	tcsetattr(STDIN_FILENO, TCSANOW, &oldt);
	fcntl(STDIN_FILENO, F_SETFL, oldf);

	if (ch != EOF)
	{
		ungetc(ch, stdin);
		return 1;
	}

	return 0;
#endif
}

