void comanda_go(char * strcommanda) {
	ssbase[0].temps_acabat = false;
	tasca = tasca_analitzar;

	std::string comanda(strcommanda);

	temps_tipus = 0;
	
	if (comanda.find("infinite") != std::string::npos)
	{
		temps_tipus |= TipusInfinit;
	}
	if (comanda.find("depth") != std::string::npos)
	{
		temps_tipus |= TipusProfunditat;
		recupera_valor(strcommanda, "depth", &temps_Profunditat);
	}
	if (comanda.find("nodes") != std::string::npos)
	{
		temps_tipus |= TipusNodes;
		recupera_valor(strcommanda, "nodes", &temps_nodes);
	}
	if (comanda.find("ponder") != std::string::npos)
	{
		temps_tipus |= TipusInfinit;
		tasca = tasca_ponderar;
	}
	if (comanda.find("wtime") != std::string::npos)
	{
		temps_tipus |= TipusTemps;
		recupera_valor(strcommanda, "wtime", &temps_temps[blanques]);
	}
	if (comanda.find("winc") != std::string::npos)
	{
		temps_tipus |= TipusIncrement;
		recupera_valor(strcommanda, "winc", &temps_inc[blanques]);
	}
	if (comanda.find("btime") != std::string::npos)
	{
		temps_tipus |= TipusTemps;
		recupera_valor(strcommanda, "btime", &temps_temps[negres]);
	}
	if (comanda.find("binc") != std::string::npos)
	{
		temps_tipus |= TipusIncrement;
		recupera_valor(strcommanda, "binc", &temps_inc[negres]);
	}
	if (comanda.find("movestogo") != std::string::npos)
	{
		temps_tipus |= TipusNumMoviments;
		recupera_valor(strcommanda, "movestogo", &temps_NumMoviments);
	}
	if (comanda.find("movetime") != std::string::npos)
	{
		temps_tipus |= TipusTempsMoviment;
		int tm;
		recupera_valor(strcommanda, "movetime", &tm);
		temps_TempsMoviment = tm;
	}

	if (temps_tipus == 0)
		temps_tipus = TipusInfinit;
}

