	posathistoricobertures = false;
	for (i = 0; i < numcje; i++){
		if (Tpeons(blanques) == cje[i].peonsblanques && Tpeons(negres) == cje[i].peonsnegres) {
			for (j = 0; j < jugadesguardades; j++) {
				int peca = 0;
				int casella = 0;
				//posar peça
				for (k = 0; k < 4; k++) {
					if (BIT_ISSET(cje[i].jugades, bitactual)) {
						peca |= Bit(k);
					}
					bitactual++;
				}
				if (peca == 0)
					return;
				//posar casella destí
				for (k = 0; k < 6; k++) {
					if (BIT_ISSET(cje[i].jugades, bitactual)) {
						casella |= Bit(k);
					}
					bitactual++;
				}
				if (casella == 0) //???
					return;
				posathistoricobertures = true;
				SumaHistorial(peca, casella, (10 - j) * 15);		// *15 //no: *5, *10, *13, *17, *20, *25
			}
		}
	}
